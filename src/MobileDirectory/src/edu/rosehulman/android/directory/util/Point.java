package edu.rosehulman.android.directory.util;

/** Represents a point in 2D space */
public class Point {
	
	public int x;
	public int y;
	
	/**
	 * Creates a new Point
	 * 
	 * @param x The x coordinate
	 * @param y The y coordinate
	 */
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

}
